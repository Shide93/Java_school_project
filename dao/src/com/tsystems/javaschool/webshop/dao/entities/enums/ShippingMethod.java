package com.tsystems.javaschool.webshop.dao.entities.enums;

/**
 * Enumeration represents possible shipping methods.
 */
public enum ShippingMethod {
    /**
     * shipping by post.
     */
    POST,

    /**
     * shipping by DHL company.
     */
    DHL,

    /**
     * courier delivery.
     */
    COURIER,

    /**
     * pickup by customer.
     */
    PICKUP
}
